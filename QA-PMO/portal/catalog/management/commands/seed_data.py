class Command(BaseCommand):
    help = "静的版のサービス・観点ライブラリ・欠陥パターンをDBへ投入する"

    @transaction.atomic
    def handle(self, *args, **options):
        # 既存削除（開発用・冪等）
        Service.objects.all().delete()
        ServiceGroup.objects.all().delete()
        Category.objects.all().delete()
        Viewpoint.objects.all().delete()
        DefectPattern.objects.all().delete()
        ViewpointCategory.objects.all().delete()

        # 区分
        cat_map = {}
        for slug, name, icon, tagline, order in CATEGORIES:
            cat_map[slug] = Category.objects.create(
                slug=slug, name=name, icon=icon, tagline=tagline, order=order)

        # グループ
        grp_map = {}
        for cat_slug, names in GROUPS.items():
            for i, name in enumerate(names):
                grp_map[(cat_slug, name)] = ServiceGroup.objects.create(
                    category=cat_map[cat_slug], name=name, order=i + 1)

        # サービス
        for order, s in enumerate(SERVICES, start=1):
            slug, title, cat, grp, icon, product, kind, tool_key, summary, tags, features, steps = s
            Service.objects.create(
                slug=slug, title=title, category=cat_map[cat],
                group=grp_map.get((cat, grp)) if grp else None,
                icon=icon, product=product, kind=kind, tool_key=tool_key,
                summary=summary, description=summary, tags=tags,
                features=features, steps=steps, order=order)

        # 観点カテゴリ
        vpcat_map = {}
        for i, (code, name) in enumerate(VP_CATEGORIES, start=1):
            vpcat_map[code] = ViewpointCategory.objects.create(code=code, name=name, order=i)

        # 観点
        def add_vps(items, source_type, source_key=""):
            for vp, tech, cat_code in items:
                Viewpoint.objects.create(
                    category=vpcat_map[cat_code], viewpoint=vp, technique=tech,
                    source_type=source_type, source_key=source_key)

        add_vps(VP_ALWAYS, Viewpoint.SOURCE_ALWAYS)
        for k, items in VP_FIELD.items():
            add_vps(items, Viewpoint.SOURCE_FIELD, k)
        for k, items in VP_FLAG.items():
            add_vps(items, Viewpoint.SOURCE_FLAG, k)
        for k, items in VP_INDUSTRY.items():
            add_vps(items, Viewpoint.SOURCE_INDUSTRY, k)

        # 欠陥パターン
        for pid, cat_code, pattern, example, prevention in DEFECT_PATTERNS:
            DefectPattern.objects.create(
                pattern_id=pid, category=vpcat_map[cat_code],
                pattern=pattern, example=example, prevention=prevention)

        self.stdout.write(self.style.SUCCESS(
            f"投入完了: 区分{Category.objects.count()} / "
            f"サービス{Service.objects.count()} / "
            f"観点{Viewpoint.objects.count()} / "
            f"欠陥パターン{DefectPattern.objects.count()}"))
