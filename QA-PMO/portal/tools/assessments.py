def get_model(key):
    return MODELS.get(key)


def scale_of(model):
    return model.get("scale", DEFAULT_SCALE)


def item_keys(model):
    """フォームの各項目に対応するキー一覧。"""
    out = []
    for area in model["areas"]:
        for i, item in enumerate(area["items"]):
            out.append({"area": area["code"], "idx": i,
                        "name": f"a_{area['code']}_{i}", "item": item})
    return out


def blank_responses(model, default=1):
    return {k["name"]: default for k in item_keys(model)}


def _band_for(model, score):
    for threshold, label, desc, tone in model["bands"]:
        if score >= threshold:
            return {"label": label, "desc": desc, "tone": tone, "threshold": threshold}
    last = model["bands"][-1]
    return {"label": last[1], "desc": last[2], "tone": last[3], "threshold": last[0]}


def assess(model, responses, satisfied=2):
    """responses: {a_<AREA>_<idx>: 0..3} → 評価結果 dict。

    欠損・範囲外は 0 に丸める（堅牢性）。satisfied 未満の項目を改善提案に集約。
    """
    areas = []
    recommendations = []
    for area in model["areas"]:
        items = []
        vals = []
        for i, item in enumerate(area["items"]):
            raw = responses.get(f"a_{area['code']}_{i}", 0)
            try:
                v = int(raw)
            except (TypeError, ValueError):
                v = 0
            v = max(0, min(MAX_PER_ITEM, v))
            vals.append((v, item.get("weight", 1)))
            ok = v >= satisfied
            items.append({"q": item["q"], "value": v, "satisfied": ok,
                          "fix": item["fix"], "weight": item.get("weight", 1)})
            if not ok:
                recommendations.append({
                    "area": area["name"], "area_code": area["code"],
                    "q": item["q"], "fix": item["fix"], "value": v,
                    "weight": item.get("weight", 1),
                    "authority": area["authority"],
                })
        wsum = sum(w for _, w in vals)
        score = round(sum(v * w for v, w in vals) / (wsum * MAX_PER_ITEM) * 100) if wsum else 0
        areas.append({
            "code": area["code"], "name": area["name"], "authority": area["authority"],
            "score": score, "satisfied": score >= 67, "items": items,
        })

    overall = round(sum(a["score"] for a in areas) / len(areas)) if areas else 0
    band = _band_for(model, overall)

    # 改善提案を「重み降順 → スコア昇順」で並べる（重要かつ弱い項目を先に）
    recommendations.sort(key=lambda r: (-r["weight"], r["value"]))
    for idx, r in enumerate(recommendations, start=1):
        r["priority"] = "最優先" if idx <= 3 else ("優先" if idx <= 6 else "推奨")
        r["rank"] = idx

    chart = {
        "type": "radar",
        "data": {
            "labels": [a["name"] for a in areas],
            "datasets": [
                {"label": "現状スコア(%)",
                 "data": [a["score"] for a in areas],
                 "backgroundColor": "rgba(26,58,107,0.15)",
                 "borderColor": "#1a3a6b", "pointBackgroundColor": "#1a3a6b"},
                {"label": "目標(80%)",
                 "data": [80 for _ in areas],
                 "backgroundColor": "rgba(31,157,99,0.04)",
                 "borderColor": "#1f9d63", "borderDash": [5, 4],
                 "pointRadius": 0, "fill": False},
            ],
        },
        "options": {
            "responsive": True, "maintainAspectRatio": False,
            "scales": {"r": {"beginAtZero": True, "max": 100, "ticks": {"stepSize": 20}}},
            "plugins": {"legend": {"position": "bottom"},
                        "title": {"display": True, "text": model["title"]}},
        },
    }

    return {
        "title": model["title"], "lead": model["lead"], "standard": model["standard"],
        "band_label": model["band_label"],
        "areas": areas, "overall_score": overall, "band": band,
        "recommendations": recommendations, "n_findings": len(recommendations),
        "chart": chart, "satisfied": satisfied,
    }
